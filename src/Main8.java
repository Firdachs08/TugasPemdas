package operatorrelasional;
public class Main8 {
    public static void main(String[] args) 
    {
     // create variables 
        int a = 7, b = 11;
        
     // value of a and b
        System.out.println("a adalah " + a + " dan b adalah " + b);
        
     // == operator
        System.out.println("a == b :");
        System.out.println(a == b);     // false
        
     // != operator
        System.out.println("a != b :");
        System.out.println(a != b);     // true
        
     // > operator
        System.out.println("a > b :");
        System.out.println(a > b);     // false
        
     // < operator
        System.out.println("a < b :");
        System.out.println(a < b);     // true
        
     // >= operator
        System.out.println("a >= b :");
        System.out.println(a >= b);     // false
        
      // <= operator
        System.out.println("a <= b :");
        System.out.println(a <= b);     // false
    } 
}
