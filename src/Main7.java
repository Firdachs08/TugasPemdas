package operatorpenugasan;
public class Main7 {
    public static void main(String[] args){
        // create variables 
        int a = 4;
        int b;
        
        System.out.println("a = 4");
        // assign value using = 
        b = a;
        System.out.println("b = a : " + b);
        
        // assign value using =+
        b += a;
        System.out.println("b += a : " + b);
        
        // assign value using =* 
        b *= a;
        System.out.println("b *= a : " + b);
    }
}
